
public class CalcFunctions {
	//addition function
	public int sum (int x, int y){
		int res = x + y;
		return res;
	}
	//subtraction function
	public int subtract (int x, int y){
		int res = x - y;
		return res;
	}
	//multiple function
	public int multiply (int x, int y) {
		int res = x * y;
		return res;
	}
	//division function
	public double divide (int x, int y){
		double res = x/y;
		return res; 
	}
}